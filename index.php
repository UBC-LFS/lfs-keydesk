<h2>Key Requisition Process</h2>
<p>
After this form has been submitted, it will be reviewed and confirmed with
your supervisor that the access requested is appropriate.
</p>

<p>
On success, a requisition will be made with the UBC Parking and Access Control
Services.  You will receive an email from them alerting you to pick up your
keys when they are ready.
</p>

<p>
This LFS key desk application form also sends you an email when you click on
submit.  If you have any questions regarding the key application, or if you have
not received an email from UBC Keydesk for pick-up after a week, please reply
to this ticket.
</p>

<h4>UBC Key Desk Details</h4>
<p>
<ul>
  <li>
  The key requisition is valid for thirty days from date of application.
  </li>
  <li>
  A refundable deposit is required for each key to a maximum of $80.00.
  </li>
  <li>
  UBC key control may require up to <b>one week</b> from the date they
  receive the key requisition to provide the requested key(s).  
  </li>
</ul>
</p>

<h2>UBCcard Setup Procedures</h2>
<p>

<h3>UBC Students:</h3>
<ol>
<li>Submit request form.
<li>Request is checked with supervisor/supervisor of area.
<li>Card is programmed once the supervisor(s) approves.
</ol>
</p>

<h3>Staff/Faculty:</h3>
<ol>
<li>Submit request form.
<li>Request is checked with supervisor/supervisor of area.
<li>Once the request is approved by the supervisor(s), the card may be programmed.
<li>If the requestor's card has not been programmed in the past, they will
need to bring the card to the Learning Centre in MCML for MCML requests or to
FNH Main office for FNH requests. If the card has been programmed in the past,
this will not be necessary.

</ol>
</p>

